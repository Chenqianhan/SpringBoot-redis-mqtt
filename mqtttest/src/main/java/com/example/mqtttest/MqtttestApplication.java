package com.example.mqtttest;

import org.eclipse.paho.client.mqttv3.*;
import org.eclipse.paho.client.mqttv3.persist.MemoryPersistence;
import org.springframework.boot.SpringApplication;
import org.springframework.boot.autoconfigure.SpringBootApplication;

@SpringBootApplication
public class MqtttestApplication {

    public static void main(String[] args) throws MqttException {
        SpringApplication.run(MqtttestApplication.class, args);
        String url="tcp://30.207.66.223";
        String topic="test";
        int qos=1;
        String clientId="receiverChen";
        String username="admin";
        String password="password";
        String content="Can you hear me?";

        try {
            MqttClient mqttClient=new MqttClient(url, clientId, new MemoryPersistence());
            //连接设置
            MqttConnectOptions options=new MqttConnectOptions();
            //设置是否清空session,这里如果设置为false表示服务器会保留客户端的连接记录，这里设置为true表示每次连接到服务器都以新的身份连接
            options.setCleanSession(true);
            options.setUserName(username);
            options.setPassword(password.toCharArray());
            //设置超时时间
            options.setConnectionTimeout(10);
            //设置会话心跳时间 单位为秒 服务器会每隔1.5*20秒的时间向客户端发送个消息判断客户端是否在线，但这个方法并没有重连的机制
            options.setKeepAliveInterval(30);

            //Subscriber's，设置回调函数
            mqttClient.setCallback(new MqttCallback() {
                @Override
                public void connectionLost(Throwable throwable) {
                    System.out.println("Connection Lost.");

                }

                @Override
                public void messageArrived(String topic, MqttMessage message) throws Exception {
                    System.out.println("topic: "+topic);
                    //设置消息服务质量
                    System.out.println("Qos: "+message.getQos());
                    System.out.println("message: "+new String(message.getPayload()));
                }

                @Override
                public void deliveryComplete(IMqttDeliveryToken token) {
                    System.out.println("Delivery completed --------"+ token.isComplete());
                }
            });

            //建立连接
            mqttClient.connect(options);

            //Subscriber's，订阅
            mqttClient.subscribe(topic,qos);

            //Publisher's，创建消息
            MqttMessage message=new MqttMessage(content.getBytes());
            //Publisher's发布消息
            mqttClient.publish(topic,message);
        }catch (Exception e) {
            e.printStackTrace();
        }
    }

}

